    private void LoginLoginButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_LoginLoginButtonMouseClicked
        TextFieldsCorrectnessController c1 = new TextFieldsCorrectnessController(); 
        c1.checkCorrectnessLogin(LoginUsernameTextbox, LoginPasswordTextbox);
        if(c1.isGood())
        {
            DBConnectionController c = new DBConnectionController();
            boolean check =c.checkUser(LoginUsernameTextbox, LoginPasswordTextbox,LoginPeselTextbox1);
            if(check)
            {
                LoginUsernameTextbox.setText("");
                LoginPasswordTextbox.setText("");
                this.dispose();
                af.setVisible(true);
            }
            else
            {
                wrongpass.setVisible(true);
                LoginUsernameTextbox.setText("");
                LoginPasswordTextbox.setText("");
            }
        }
        
    }//GEN-LAST:event_LoginLoginButtonMouseClicked

    private void LoginUsernameTextboxKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_LoginUsernameTextboxKeyTyped
        char c = evt.getKeyChar();
        if(!(Character.isDigit(c)|| (c==KeyEvent.VK_BACKSPACE) || (c==KeyEvent.VK_DELETE)))
        {
            evt.consume();
            mf.setVisible(true);
        }
    }//GEN-LAST:event_LoginUsernameTextboxKeyTyped

